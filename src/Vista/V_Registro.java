    private void R_BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_R_BtnRegistrarActionPerformed
        // CODIGO PARA EL BOTON REGISTRARSE

        if (R_Nombre.getText().isEmpty() || R_Apellido.getText().isEmpty() ||
            R_Documento.getText().isEmpty() || R_Telefono.getText().isEmpty() ||
            R_Correo.getText().isEmpty() || R_Contraseña.getText().isEmpty()    
                ) {
            JOptionPane.showMessageDialog(null, "Faltan campos por llenar");

        } else {
            C_Usuario cu = new C_Usuario();
            cu.setIu_registro(this);
            cu.Registro();
        }
    }//GEN-LAST:event_R_BtnRegistrarActionPerformed

