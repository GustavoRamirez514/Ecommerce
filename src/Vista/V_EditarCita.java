    private void E_BtnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_E_BtnAgendarActionPerformed
        if (E_Cliente.getText().isEmpty() || E_Agente.getText().isEmpty()
                || E_Observaciones.getText().isEmpty() || E_Fecha.getDate()== null) {
            JOptionPane.showMessageDialog(null, "Faltan campos por llenar");

        } else if (JOptionPane.showConfirmDialog(null, "Se agregara el registro, Â¿desea continuar?") == JOptionPane.YES_OPTION) {
            cu.ActualizarCita();
            this.dispose();
        }
    }//GEN-LAST:event_E_BtnAgendarActionPerformed

    public void FechaActual() {
        Date date = new Date();
        E_Fecha.getDayChooser().setMinSelectableDate(date);
    }
