    public void HabilitarMenu(String Nombre) {

        for (int i = 0; i < MenuBarra.getMenuCount(); i++) {
            if (MenuBarra.getMenu(i).getActionCommand().equals(Nombre)) {
                MenuBarra.getMenu(i).setVisible(true);

            }
        }

    }

